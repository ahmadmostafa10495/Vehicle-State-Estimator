ERROR_STATUS SPIIf_init(void)
{
	SPI_Cfg_s Spi_info={MASTER,MODE_0,MSB,POLLING,SPI_PRESCALER_4};
	SPI_Init(&Spi_info);
	return E_OK;
}
	



ERROR_STATUS SPIIf_update(uint8 *Data)
{
	SPI_MasterTransmission(Data);
	return E_OK;
}